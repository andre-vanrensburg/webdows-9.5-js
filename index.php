	  <h2>Portfolio:</h2>
		<div class="col-lg-10 col-lg-offset-1">
		
		<p><strong>Projects:</br>

Name: Emergency Response Web Application</br>
Designation: Full Stack Developer</br>
Languages: HTML, CSS, JS, PHP, XML & SQL</br>
</br>
Client requirements:</strong></br>
The client approached me to have an emergency response web application developed as an added value service to their clients. The core function of the application was to provide assistance to members when in an emergency, an emergency being defined as any event which left the sender unable to resolve without assistance, e.g. a flood, fire or farm attack. Members formed part of groups in communities in their immediate surroundings from regional down to town/city level e.g. Western Cape and George, with George being a group within a larger group. </br>
</br><strong>Solution:</strong></br>
I developed a web application that integrated with a 3rd party SMS gateway service provider, called Stouf Communications, via API calls. It works by use of a generic number provided by the gateway provider which receives ("catches") emergency SMS’s from members, and then passes it via an API call to the database to query all members in the sender’s area, generating an emergency SMS to them with all their information stored on the system e.g. contact number, address, alternative contacts, medical information, etc. The database queries also get cached using redis for quicker response and reduced load on the server and the database is normalized down to city level to prevent iteration of members outside the sender’s immediate area.</br>
</br>
The system has both an administrative panel and a member’s panel. The administrators have access to manage membership via CRUD functionality, and also to communicate with members via SMS on any level be that individually, regionally, or via their respective town/city groups and also with area leaders e.g. commandos, lieutenants etc. The members panel allows a member to maintain their information e.g. address, contact, medical information etc. The system uses Datatables JS with the ability to print, sort, filter, search, export and do live edits on records with AJAX calls. </br>
</br>
<strong>Comments:</strong></br>
I enjoyed this project a lot, and it probably ranks in the top 3 for me due to the level of technicality, but most importantly that it serves for a very good cause as it saves lives, and it has. I would have enjoyed working further on this project to also develop a mobile application version and migrate to push notifications instead of SMS, but at the time of development I was still busy studying mobile app development and couldn't use the project as a learning curve, due to its nature of course, a mistake could cost a life. There was of course the hybrid app route, or a web view, but the client needed to tap into the native software features of Android and iOS, so I turned the project down and offered to instead serve as the project lead to manage the mobile app development.</br>
</br>
